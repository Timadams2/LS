require 'pry'

def neutralize(sentence)
  words = []
  words.each do |word|
    binding.pry
    words.push(word) unless negative?(word)
  end

  words.join(' ')
end

def negative?(word)
  [ 'dull',
    'boring',
    'annoying',
    'chaotic'
  ].include?(word)
end

